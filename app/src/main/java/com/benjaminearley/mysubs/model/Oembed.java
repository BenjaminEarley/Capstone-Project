package com.benjaminearley.mysubs.model;


public class Oembed {

    private String providerUrl;
    private String description;
    private String title;
    private String type;
    private int thumbnailWidth;
    private int height;
    private int width;
    private String html;
    private String version;
    private String providerName;
    private String thumbnailUrl;
    private int thumbnailHeight;

    /**
     * @return The providerUrl
     */
    public String getProviderUrl() {
        return providerUrl;
    }

    /**
     * @param providerUrl The provider_url
     */
    public void setProviderUrl(String providerUrl) {
        this.providerUrl = providerUrl;
    }

    /**
     * @return The description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description The description
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return The title
     */
    public String getTitle() {
        return title;
    }

    /**
     * @param title The title
     */
    public void setTitle(String title) {
        this.title = title;
    }

    /**
     * @return The type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type The type
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return The thumbnailWidth
     */
    public int getThumbnailWidth() {
        return thumbnailWidth;
    }

    /**
     * @param thumbnailWidth The thumbnail_width
     */
    public void setThumbnailWidth(int thumbnailWidth) {
        this.thumbnailWidth = thumbnailWidth;
    }

    /**
     * @return The height
     */
    public int getHeight() {
        return height;
    }

    /**
     * @param height The height
     */
    public void setHeight(int height) {
        this.height = height;
    }

    /**
     * @return The width
     */
    public int getWidth() {
        return width;
    }

    /**
     * @param width The width
     */
    public void setWidth(int width) {
        this.width = width;
    }

    /**
     * @return The html
     */
    public String getHtml() {
        return html;
    }

    /**
     * @param html The html
     */
    public void setHtml(String html) {
        this.html = html;
    }

    /**
     * @return The version
     */
    public String getVersion() {
        return version;
    }

    /**
     * @param version The version
     */
    public void setVersion(String version) {
        this.version = version;
    }

    /**
     * @return The providerName
     */
    public String getProviderName() {
        return providerName;
    }

    /**
     * @param providerName The provider_name
     */
    public void setProviderName(String providerName) {
        this.providerName = providerName;
    }

    /**
     * @return The thumbnailUrl
     */
    public String getThumbnailUrl() {
        return thumbnailUrl;
    }

    /**
     * @param thumbnailUrl The thumbnail_url
     */
    public void setThumbnailUrl(String thumbnailUrl) {
        this.thumbnailUrl = thumbnailUrl;
    }

    /**
     * @return The thumbnailHeight
     */
    public int getThumbnailHeight() {
        return thumbnailHeight;
    }

    /**
     * @param thumbnailHeight The thumbnail_height
     */
    public void setThumbnailHeight(int thumbnailHeight) {
        this.thumbnailHeight = thumbnailHeight;
    }

}
